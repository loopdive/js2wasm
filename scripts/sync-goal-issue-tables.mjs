#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import fs from "fs";
import path from "path";

const ROOT = process.cwd();
const ISSUES_DIR = path.join(ROOT, "plan", "issues");
const GOALS_DIR = path.join(ROOT, "plan", "goals");
const START = "<!-- AUTOGENERATED:GOAL-ISSUES-START -->";
const END = "<!-- AUTOGENERATED:GOAL-ISSUES-END -->";

const STATUS_PRIORITY = {
  done: 0,
  "wont-fix": 1,
  blocked: 2,
  review: 3,
  "in-progress": 4,
  ready: 5,
  deferred: 6,
  backlog: 7,
};

function git(args) {
  return execFileSync("git", args, {
    cwd: ROOT,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "ignore"],
  }).trim();
}

function getTrackedMarkdownFiles(root) {
  try {
    return new Set(
      git(["ls-files", root])
        .split("\n")
        .map((file) => file.trim())
        .filter((file) => file.endsWith(".md"))
        .map((file) => path.join(ROOT, file)),
    );
  } catch {
    return null;
  }
}

function issueStatusPriority(status) {
  return STATUS_PRIORITY[status] ?? 8;
}

function walk(dir, out = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, out);
    else if (entry.isFile() && entry.name.endsWith(".md")) out.push(full);
  }
  return out;
}

function isIssueFile(file) {
  return /^\d+[a-z]?(?:-.+)?\.md$/i.test(path.basename(file));
}

function parseFrontmatter(text) {
  const match = text.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return {};
  const data = {};
  let currentKey = null;
  for (const line of match[1].split("\n")) {
    const kv = line.match(/^([A-Za-z_][\w-]*):\s*(.*)$/);
    if (kv) {
      currentKey = kv[1];
      const raw = kv[2].trim();
      if (!raw) data[currentKey] = [];
      else if (raw.startsWith("[")) {
        try {
          data[currentKey] = JSON.parse(raw);
        } catch {
          data[currentKey] = raw;
        }
      } else {
        data[currentKey] = raw.replace(/^"|"$/g, "");
      }
      continue;
    }
    const li = line.match(/^\s*-\s*(.*)$/);
    if (li && currentKey && Array.isArray(data[currentKey])) {
      data[currentKey].push(li[1].replace(/^"|"$/g, ""));
    }
  }
  return data;
}

function issueIdFromPath(file) {
  return path.basename(file, ".md");
}

function normalizeStatus(rawStatus) {
  const status = String(rawStatus || "").trim();
  if (status === "in_progress") return "in-progress";
  if (status === "in-review" || status === "in_review") return "review";
  if (status) return status;
  return "ready";
}

function sprintFromPath(file) {
  const m = file.match(/\/sprints\/(\d+)\//);
  if (m) return m[1];
  if (/\/backlog\//.test(file)) return "Backlog";
  return "";
}

const trackedFiles = getTrackedMarkdownFiles("plan/issues");
const files = walk(ISSUES_DIR)
  .filter(isIssueFile)
  .filter((file) => !trackedFiles || trackedFiles.has(file));

const byId = new Map();
for (const file of files) {
  const text = fs.readFileSync(file, "utf8");
  const fm = parseFrontmatter(text);
  if (!fm.title && !fm.status && !fm.id) continue;
  const goal = String(fm.goal || "").trim();
  if (!goal) continue;
  const status = normalizeStatus(fm.status);
  const issue = {
    id: String(fm.id || issueIdFromPath(file)),
    title: String(fm.title || "").replace(/\|/g, "\\|"),
    sprint: String(fm.sprint || sprintFromPath(file)),
    status,
    priority: String(fm.priority || ""),
    goal,
    file: path.relative(ROOT, file),
  };
  const existing = byId.get(issue.id);
  if (!existing || issueStatusPriority(issue.status) < issueStatusPriority(existing.status)) {
    byId.set(issue.id, issue);
  }
}

const grouped = new Map();
for (const issue of byId.values()) {
  const goal = String(issue.goal || "").trim();
  if (!grouped.has(goal)) grouped.set(goal, []);
  grouped.get(goal).push(issue);
}

for (const file of fs.readdirSync(GOALS_DIR).filter((name) => name.endsWith(".md") && name !== "goal-graph.md")) {
  const goal = file.replace(/\.md$/, "");
  const goalPath = path.join(GOALS_DIR, file);
  const text = fs.readFileSync(goalPath, "utf8");
  const issues = (grouped.get(goal) || []).sort((a, b) => {
    const ai = Number.parseInt(a.id, 10);
    const bi = Number.parseInt(b.id, 10);
    if (!Number.isNaN(ai) && !Number.isNaN(bi) && ai !== bi) return ai - bi;
    return a.id.localeCompare(b.id);
  });

  const table = [
    START,
    "",
    "| # | Title | Sprint | Status | Priority |",
    "|---|-------|--------|--------|----------|",
    ...issues.map(
      (issue) => `| **${issue.id}** | ${issue.title} | ${issue.sprint} | ${issue.status} | ${issue.priority} |`,
    ),
    "",
    END,
  ].join("\n");

  const issuesSection = text.match(/## Issues\n[\s\S]*?(?=\n## |\s*$)/);
  if (!issuesSection) continue;

  const next = text.replace(issuesSection[0], `## Issues\n\n${table}\n`);
  if (next !== text) fs.writeFileSync(goalPath, next);
}

console.log(`Synced goal issue tables for ${grouped.size} goals`);
